    // Phương thức tải và cập nhât combo box chứa tên NCC
    public void loadComboBox() {
        try {
            SupplierDAO supplierDAO = new SupplierDAO();
            suppCombo.setModel(supplierDAO.setComboItems(supplierDAO.getQueryResult()));
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    // Phương thức tải dữ liệu vào bảng
    public void loadDataSet() {
        try {
            ProductDAO productDAO = new ProductDAO();
            purchaseTable.setModel(productDAO.buildTableModel(productDAO.getInputInfo()));
        } catch (SQLException throwables) {
            throwables.printStackTrace();
        }
    }

    

