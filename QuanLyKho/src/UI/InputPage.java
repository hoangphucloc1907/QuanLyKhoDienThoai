    private void purchaseButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_purchaseButtonActionPerformed
        productDTO = new ProductDTO();
        if (codeText.getText().equals("") || quantityText.getText().equals(""))
            JOptionPane.showMessageDialog(null, "Vui lòng điền đầy đủ thông tin.");
        else {
            productDTO.setSuppCode(new ProductDAO().getSuppCode(suppCombo.getSelectedItem().toString()));
            productDTO.setProdCode(codeText.getText());
            try {
                ResultSet resultSet = new ProductDAO().getProdName(codeText.getText());
                if (resultSet.next()) {
                    productDTO.setProdCode(codeText.getText());
                    productDTO.setQuantity(Integer.parseInt(quantityText.getText()));
                    productDTO.setInputPrice(Double.parseDouble(inputText.getText()));
                    new ProductDAO().addInputInfoDAO(productDTO);
                    loadDataSet();
                } else
                    JOptionPane.showMessageDialog(null, "Vui lòng thêm sản phẩm này vào \"SẢN PHẢM\" trước khi tiếp tục.");
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_purchaseButtonActionPerformed

