    private void sellButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sellButtonActionPerformed
        if (custCodeText.getText().equals("") || prodCodeText.getText().equals("")
            || quantityText.getText().equals("") || priceText.getText().equals(""))
        JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.");
        else {
            try {
                ResultSet resultSet = new CustomerDAO().getCustName(custCodeText.getText());
                if (resultSet.next()) {
                    ProductDTO productDTO = new ProductDTO();
                    productDTO.setCustCode(custCodeText.getText());
                    productDTO.setProdCode(prodCodeText.getText());
                    productDTO.setQuantity(Integer.parseInt(quantityText.getText()));
                    productDTO.setOutputPrice(Double.parseDouble(priceText.getText()));
                    new ProductDAO().outputProductDAO(productDTO);
                    loadDataSet();
                } else
                JOptionPane.showMessageDialog(this, "Khách hàng này không tồn tại "
                                                                + "Vui lòng kiểm tra mã khách hàng.");
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_sellButtonActionPerformed

