    // Phương thức tải dữ liệu vào bảng
    public void loadDataSet() {
        try {
            ProductDAO productDAO = new ProductDAO();
            salesTable.setModel(productDAO.buildTableModel(productDAO.getOutputInfo()));
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }

    

